#include <iostream>
#include <cmath>
using namespace std;
int main() {
    string itemName;
    int quantity;
    float cost;
    double tax;
    float totalcost;

    cout << "Please enter the book's name: ";
    cin >> itemName;
    cout << "Please enter how many copies of this book you've taken: ";
    cin >> quantity;
    cout << "Please enter the cost of each book: ";
    cin >> cost;

    tax = cost * 0.07;
    totalcost = (quantity * cost) + tax;

    cout << "Total cost for " << itemName << ": $" << totalcost << endl;
    
    return 0;
}